/**
 * Extract lines from a blockquote section in the markdown.
 * Looks for the section header, then collects subsequent lines that start with `>`.
 */
function extractBlockquoteSection(markdown: string, sectionHeader: string): string[] {
  const lines: string[] = [];
  const headerPattern = new RegExp(`###?\\s*\\d*\\.?\\s*${sectionHeader}`, 'i');
  const allLines = markdown.split('\n');
  const headerIdx = allLines.findIndex((l) => headerPattern.test(l));
  if (headerIdx < 0) return lines;
  for (let i = headerIdx + 1; i < allLines.length; i++) {
    const l = allLines[i];
    if (l.trim().startsWith('#')) break;
    if (l.trim().startsWith('>')) {
      const cleaned = l.replace(/^\s*>\s?/, '').replace(/\*\*/g, '').trim();
      if (cleaned) lines.push(cleaned);
    }
  }
  return lines;
}

// ─── CSV exporter for new production templates ──────────────────────────
// Single-Talent, Filmed Podcast, Packaging/Employee all use a similar
// structure: strategy header block → optional profile blocks → body table.
// We use a single configurable CSV builder.

interface CsvTemplateConfig {
  title: string;
  /** Sections to extract as key-value blocks. Each is [section header in markdown, label]. */
  kvSections: Array<{ markdownHeader: string; csvLabel: string; fields: string[] }>;
  /** Body table — section header in markdown + columns to emit. */
  bodyTable: { markdownHeader: string; columns: string[] };
  /** Optional secondary table (e.g., Hooks for Single-Talent). */
  secondaryTable?: { markdownHeader: string; columns: string[] };
}

function buildCsv(markdown: string, config: CsvTemplateConfig, product: string, adType: string): string {
  const lines = markdown.split('\n');

  // Cell escape
  const esc = (v: string) => {
    const s = (v ?? '').toString().trim();
    return s.includes(',') || s.includes('"') || s.includes('\n')
      ? `"${s.replace(/"/g, '""')}"`
      : s;
  };

  // Parse a markdown table starting at line index. Returns rows + endIdx.
  const parseTable = (startIdx: number): { rows: string[][]; endIdx: number } => {
    const rows: string[][] = [];
    let i = startIdx;
    while (i < lines.length) {
      const line = lines[i].trim();
      if (!line.startsWith('|')) break;
      if (/^\|[\s\-:|]+\|$/.test(line)) { i++; continue; }
      const cells = line.split('|').slice(1, -1).map((c) => c.replace(/\*\*/g, '').trim());
      rows.push(cells);
      i++;
    }
    return { rows, endIdx: i };
  };

  // Find a markdown heading and return its line index
  const findHeader = (header: string): number => {
    const pattern = new RegExp(`^###?\\s*\\d*\\.?\\s*${header}`, 'i');
    return lines.findIndex((l) => pattern.test(l.trim()));
  };

  // Extract a bulleted key-value block (e.g., "- **Concept:** ...") below a header
  const extractKv = (headerIdx: number): Record<string, string> => {
    const result: Record<string, string> = {};
    if (headerIdx < 0) return result;
    for (let i = headerIdx + 1; i < lines.length; i++) {
      const l = lines[i].trim();
      if (l.startsWith('#')) break;
      const m = l.match(/^[-*]\s*\*\*([^:*]+?):\*\*\s*(.+)$/);
      if (m) result[m[1].trim()] = m[2].replace(/\*\*/g, '').trim();
    }
    return result;
  };

  // Parse a kv-table style block (Field | Value rows) below a header
  const extractKvTable = (headerIdx: number): Record<string, string> => {
    const result: Record<string, string> = {};
    if (headerIdx < 0) return result;
    for (let i = headerIdx + 1; i < lines.length; i++) {
      const l = lines[i].trim();
      if (l.startsWith('#')) break;
      if (!l.startsWith('|')) continue;
      if (/^\|[\s\-:|]+\|$/.test(l)) continue;
      const cells = l.split('|').filter((c) => c.trim()).map((c) => c.replace(/\*\*/g, '').trim());
      if (cells.length >= 2 && cells[0] !== 'Field') result[cells[0]] = cells[1];
    }
    return result;
  };

  const csv: string[][] = [];
  csv.push([config.title, '', '', '', '', '', '']);
  csv.push(['']);

  // KV sections — emit as 2-column label/value blocks
  for (const sec of config.kvSections) {
    const idx = findHeader(sec.markdownHeader);
    // Try bulleted first, fall back to table
    const kv = { ...extractKv(idx), ...extractKvTable(idx) };
    csv.push([sec.csvLabel.toUpperCase(), '']);
    for (const field of sec.fields) {
      csv.push([field, kv[field] || (field === 'Product' ? product : field === 'Ad Type' ? adType : '')]);
    }
    csv.push(['']);
  }

  // Optional secondary table (e.g., Hooks)
  if (config.secondaryTable) {
    const idx = findHeader(config.secondaryTable.markdownHeader);
    csv.push([config.secondaryTable.markdownHeader.replace(/[*\\]/g, '').toUpperCase()]);
    csv.push(config.secondaryTable.columns);
    if (idx >= 0) {
      for (let i = idx + 1; i < lines.length; i++) {
        if (lines[i].trim().startsWith('|')) {
          const { rows, endIdx } = parseTable(i);
          for (let r = 1; r < rows.length; r++) {
            const row = rows[r];
            // Pad/trim to expected column count
            while (row.length < config.secondaryTable.columns.length) row.push('');
            csv.push(row.slice(0, config.secondaryTable.columns.length));
          }
          i = endIdx;
          break;
        }
        if (lines[i].trim().startsWith('#')) break;
      }
    }
    csv.push(['']);
  }

  // Body table — the main script
  const bodyIdx = findHeader(config.bodyTable.markdownHeader);
  csv.push([config.bodyTable.markdownHeader.replace(/[*\\]/g, '').toUpperCase()]);
  csv.push(config.bodyTable.columns);
  if (bodyIdx >= 0) {
    for (let i = bodyIdx + 1; i < lines.length; i++) {
      if (lines[i].trim().startsWith('|')) {
        const { rows } = parseTable(i);
        for (let r = 1; r < rows.length; r++) {
          const row = rows[r];
          while (row.length < config.bodyTable.columns.length) row.push('');
          csv.push(row.slice(0, config.bodyTable.columns.length));
        }
        break;
      }
      if (lines[i].trim().startsWith('#')) break;
    }
  }

  // Build CSV string
  return csv.map((row) => row.map(esc).join(',')).join('\n');
}

function downloadCsv(csvString: string, briefId: string): void {
  const blob = new Blob([csvString], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  const safe = briefId.replace(/[\\/:*?"<>|]/g, '_');
  a.download = `${safe}.csv`;
  a.click();
  URL.revokeObjectURL(url);
}

